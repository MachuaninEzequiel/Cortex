def _get_test_command(ctx: ProjectContext) -> str:
    if ctx.stack.test_command:
        return ctx.stack.test_command
    pt = ctx.stack.language
    if pt == "python":
        return "pytest"
    if pt in ("javascript", "typescript"):
        return "npm test"
    if pt == "go":
        return "go test ./..."
    if pt == "rust":
        return "cargo test"
    if pt == "java":
        return "mvn test"
    return "echo 'no test command detected'"


def _get_lint_command(ctx: ProjectContext) -> str:
    if ctx.stack.lint_command:
        return ctx.stack.lint_command
    pt = ctx.stack.language
    if pt in ("javascript", "typescript"):
        return "npm run lint || true"
    if pt == "python":
        return "ruff check . || true"
    if pt == "go":
        return "golangci-lint run || true"
    if pt == "rust":
        return "cargo clippy || true"
    return "echo 'no lint command detected'"


def _get_audit_command(ctx: ProjectContext) -> str:
    pt = ctx.stack.language
    if pt in ("javascript", "typescript"):
        return "npm audit --omit=dev --audit-level=high || true"
    if pt == "python":
        return "pip audit || true"
    if pt == "go":
        return "govulncheck ./... || true"
    return "echo 'no audit command for this stack'"


def _get_install_command(ctx: ProjectContext) -> str:
    pm = ctx.stack.package_manager
    if pm == "npm":
        return "npm ci"
    if pm == "yarn":
        return "yarn install --frozen-lockfile"
    if pm == "pnpm":
        return "pnpm install --frozen-lockfile"
    if pm == "pip" or pm == "pipenv" or pm == "poetry":
        return "pip install -r requirements.txt"
    if pm == "go":
        return "go mod download"
    if pm == "cargo":
        return "cargo build"
    return "echo 'install dependencies'"


def _get_build_command(ctx: ProjectContext) -> str:
    if ctx.stack.build_command:
        return ctx.stack.build_command
    pt = ctx.stack.language
    if pt in ("javascript", "typescript"):
        return "npm run build"
    if pt == "python":
        return "python -m build"
    if pt == "go":
        return "go build ./..."
    if pt == "rust":
        return "cargo build --release"
    return "echo 'no build command'"


def _get_setup_language(ctx: ProjectContext) -> str:
    """Return GitHub Actions setup steps for the detected language."""
    pt = ctx.stack.language
    if pt in ("javascript", "typescript"):
        return """\
      - name: Setup Node.js environment
        uses: actions/setup-node@v4
        with:
          node-version: '20'
          cache: 'npm'
"""
    if pt == "python":
        return """\
      - name: Setup Python environment
        uses: actions/setup-python@v5
        with:
          python-version: '3.11'
"""
    if pt == "go":
        return """\
      - name: Setup Go environment
        uses: actions/setup-go@v5
        with:
          go-version: '1.22'
"""
    if pt == "java" or pt == "kotlin":
        return """\
      - name: Setup Java environment
        uses: actions/setup-java@v4
        with:
          distribution: 'temurin'
          java-version: '21'
"""
    if pt == "ruby":
        return """\
      - name: Setup Ruby environment
        uses: ruby/setup-ruby@v1
        with:
          ruby-version: '3.3'
"""
    return ""
