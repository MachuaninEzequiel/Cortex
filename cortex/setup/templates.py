def render_config_yaml(ctx: ProjectContext) -> str:
    """Render config.yaml with project-aware defaults."""
    # Choose embedding provider based on detected env vars
    provider = "none"
    model = ""
    if ctx.env.has_openai_key:
        provider = "openai"
        model = "text-embedding-3-small"

    return f"""\
# Cortex configuration — auto-generated by `cortex setup`
# Project: {ctx.stack.project_name} ({ctx.stack.language})

episodic:
  persist_dir: .memory/chroma
  collection_name: cortex_episodic
  embedding_model: all-MiniLM-L6-v2
  embedding_backend: onnx  # onnx | local | openai
  namespace_mode: project  # project | branch | custom
  namespace_value: ""

semantic:
  vault_path: vault

retrieval:
  top_k: 5
  episodic_weight: 1.0
  semantic_weight: 1.0

llm:
  provider: {provider}
  model: "{model}"

integrations:
  jira:
    enabled: false
    base_url: ""
    email_env: JIRA_EMAIL
    token_env: JIRA_API_TOKEN
"""


def render_org_yaml(ctx: ProjectContext, profile: str = "small-company") -> str:
    """Render .cortex/org.yaml with a baseline enterprise topology."""
    config = build_enterprise_org_config(
        project_name=ctx.stack.project_name or ctx.root.name,
        profile=profile,  # type: ignore[arg-type]
        github_actions_enabled=ctx.ci.has_github_actions,
        branch_isolation_enabled=False,
    )
    return render_enterprise_config_yaml(config)


